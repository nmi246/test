# this file contains common calculator functions:


def nAdd(x, y):
    # Add function
    return x + y


# subtract function
def nSubtract(x, y):
    return x - y


# Multiply function
def nMultiply(x, y):
    return x * y


# Divide function
def nDivide(x, y):
    return x / y

# remainder functions
def nRemainder(x, y):
    return x % y

# floor divide function
def nFloorDivide(x, y):
    return x // y
